#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	char	a;

	i = 0;
	(void)argc;
	while (argv[0][i] != '\0')
	{
		a = argv[0][i];
		write(1, &a, 1);
		i++;
	}
	write(1, "\n", 1);
}
